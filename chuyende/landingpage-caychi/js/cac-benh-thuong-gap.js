//Làm hiệu ứng nhấp esc tắt div
$( document ).on( 'keydown', function ( e ) {
    if ( e.keyCode === 27 ) {
		$(".dieu-tri-benh .window-info").fadeOut("fast");
		$('.dieu-tri-benh .box-info p').css('display', 'none');
    }
});